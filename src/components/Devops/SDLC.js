// =============================================================================
// 🧠 SOFTWARE DEVELOPMENT LIFE CYCLE (SDLC)
// =============================================================================

// -----------------------------------------------------------------------------
// ! WHAT IT IS
// -----------------------------------------------------------------------------
/*
 * SDLC is a structured process that defines how software is:
 * - Planned
 * - Built
 * - Tested
 * - Deployed
 * - Maintained
 *
 * It provides a predictable framework for turning
 * business requirements into working software.
 *
 * SDLC is NOT:
 * - A specific methodology
 * - A tool
 *
 * SDLC IS:
 * - The end-to-end lifecycle of software
 */

// -----------------------------------------------------------------------------
// ? WHY SDLC EXISTS (PROBLEM IT SOLVES)
// -----------------------------------------------------------------------------
/*
 * Building software without a process leads to:
 * - Unclear requirements
 * - Missed deadlines
 * - Poor quality
 * - Cost overruns
 * - Unmaintainable systems
 *
 * SDLC introduces:
 * - Structure
 * - Accountability
 * - Quality control
 * - Risk reduction
 */

// -----------------------------------------------------------------------------
// * HIGH-LEVEL FLOW (MENTAL MODEL)
// -----------------------------------------------------------------------------
/*
 * Requirements
 *     ↓
 * Design
 *     ↓
 * Development
 *     ↓
 * Testing
 *     ↓
 * Deployment
 *     ↓
 * Maintenance
 *
 * This flow can be:
 * - Linear (Waterfall)
 * - Iterative (Agile)
 * - Continuous (DevOps)
 */

// -----------------------------------------------------------------------------
// ! SDLC PHASES (CORE)
// -----------------------------------------------------------------------------

// 1️⃣ REQUIREMENTS GATHERING & ANALYSIS
/*
 * Purpose:
 * - Understand what needs to be built
 *
 * Activities:
 * - Stakeholder interviews
 * - Business requirement analysis
 * - Scope definition
 *
 * Output:
 * - Requirement specification document
 */

// -----------------------------------------------------------------------------
// 2️⃣ SYSTEM DESIGN
/*
 * Purpose:
 * - Decide HOW the system will work
 *
 * Activities:
 * - Architecture design
 * - Database schema
 * - API contracts
 * - UI/UX design
 *
 * Output:
 * - Technical design documents
 */

// -----------------------------------------------------------------------------
// 3️⃣ DEVELOPMENT (IMPLEMENTATION)
/*
 * Purpose:
 * - Build the actual software
 *
 * Activities:
 * - Writing code
 * - Code reviews
 * - Version control
 *
 * Output:
 * - Working software components
 */

// -----------------------------------------------------------------------------
// 4️⃣ TESTING
/*
 * Purpose:
 * - Ensure quality and correctness
 *
 * Types:
 * - Unit testing
 * - Integration testing
 * - System testing
 * - User acceptance testing (UAT)
 *
 * Output:
 * - Verified, bug-free software
 */

// -----------------------------------------------------------------------------
// 5️⃣ DEPLOYMENT
/*
 * Purpose:
 * - Release software to users
 *
 * Activities:
 * - Environment configuration
 * - Release management
 * - Production deployment
 *
 * Output:
 * - Live application
 */

// -----------------------------------------------------------------------------
// 6️⃣ MAINTENANCE & MONITORING
/*
 * Purpose:
 * - Keep the system running and improving
 *
 * Activities:
 * - Bug fixes
 * - Performance tuning
 * - Feature enhancements
 * - Monitoring & logging
 *
 * Output:
 * - Stable and evolving system
 */

// -----------------------------------------------------------------------------
// ! SDLC MODELS (IMPORTANT)
// -----------------------------------------------------------------------------
/*
 * Common SDLC models:
 *
 * - Waterfall
 * - Agile
 * - Iterative
 * - Spiral
 * - V-Model
 * - DevOps (Continuous SDLC)
 */

// -----------------------------------------------------------------------------
// ? SDLC VS AGILE VS DEVOPS (BIG PICTURE)
// -----------------------------------------------------------------------------
/*
 * SDLC:
 * - WHAT stages exist in software creation
 *
 * Agile:
 * - HOW stages are executed iteratively
 *
 * DevOps:
 * - HOW stages are automated and continuous
 *
 * Relationship:
 * SDLC → Agile → DevOps
 */

// -----------------------------------------------------------------------------
// ! WHERE DEVOPS FITS INTO SDLC
// -----------------------------------------------------------------------------
/*
 * Traditional SDLC:
 * - Deployment is late and risky
 *
 * DevOps-enhanced SDLC:
 * - CI/CD automates:
 *   - Build
 *   - Test
 *   - Deploy
 *
 * Monitoring feeds back into requirements.
 *
 * SDLC becomes a loop, not a straight line.
 */

// -----------------------------------------------------------------------------
// ? REAL-WORLD CONSEQUENCES OF POOR SDLC
// -----------------------------------------------------------------------------
/*
 * - Scope creep
 * - Unstable releases
 * - Security vulnerabilities
 * - High maintenance cost
 * - Burnout
 */

// -----------------------------------------------------------------------------
// * REAL-WORLD CONSEQUENCES OF GOOD SDLC
// -----------------------------------------------------------------------------
/*
 * - Predictable delivery
 * - High-quality software
 * - Easier maintenance
 * - Better team collaboration
 * - Happy stakeholders
 */

// -----------------------------------------------------------------------------
// ! COMMON MISCONCEPTIONS
// -----------------------------------------------------------------------------
/*
 * ❌ "SDLC is old-fashioned"
 * ❌ "Agile replaces SDLC"
 * ❌ "DevOps replaces SDLC"
 *
 * ✅ Truth:
 * - SDLC always exists (explicit or implicit)
 * - Agile and DevOps optimize SDLC, they don’t remove it
 */

// -----------------------------------------------------------------------------
// * ONE-LINE SENIOR SUMMARY
// -----------------------------------------------------------------------------
/*
 * SDLC defines the stages of software creation;
 * Agile optimizes development within those stages;
 * DevOps automates and connects them continuously.
 */
