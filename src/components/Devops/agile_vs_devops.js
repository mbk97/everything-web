// =============================================================================
// 🧠 AGILE VS DEVOPS
// =============================================================================

// -----------------------------------------------------------------------------
// ! WHAT THIS COMPARISON IS ABOUT
// -----------------------------------------------------------------------------
/*
 * Agile and DevOps are NOT competing methodologies.
 *
 * Agile focuses on:
 * - How software is DEVELOPED
 *
 * DevOps focuses on:
 * - How software is DELIVERED, DEPLOYED, and OPERATED
 *
 * Think:
 * Agile → Build the right thing
 * DevOps → Ship and run it right
 */

// -----------------------------------------------------------------------------
// ! AGILE — WHAT IT IS
// -----------------------------------------------------------------------------
/*
 * Agile is a software development methodology focused on:
 * - Iterative development
 * - Frequent feedback
 * - Customer collaboration
 * - Adaptability to change
 *
 * Work is broken into small chunks (sprints),
 * usually 1–4 weeks long.
 */

// * Core Agile values:
/// - Individuals & interactions over processes & tools
/// - Working software over documentation
/// - Customer collaboration over contracts
/// - Responding to change over following a plan

// -----------------------------------------------------------------------------
// ? AGILE — PROBLEM IT SOLVES
// -----------------------------------------------------------------------------
/*
 * Traditional (Waterfall) problems:
 * - Long development cycles
 * - Late discovery of bugs
 * - Changing requirements cause chaos
 *
 * Agile solution:
 * - Ship small increments
 * - Get feedback early
 * - Adjust continuously
 */

// -----------------------------------------------------------------------------
// ! DEVOPS — WHAT IT IS
// -----------------------------------------------------------------------------
/*
 * DevOps is a set of practices and culture that unifies
 * development and operations.
 *
 * Focus areas:
 * - Automation
 * - CI/CD
 * - Infrastructure
 * - Monitoring
 * - Reliability
 *
 * DevOps starts where Agile often stops.
 */

// -----------------------------------------------------------------------------
// ? DEVOPS — PROBLEM IT SOLVES
// -----------------------------------------------------------------------------
/*
 * Agile teams often say:
 * - "We finished the sprint"
 *
 * But:
 * - Code is not deployed
 * - Ops deploys once a month
 * - Releases are risky
 *
 * DevOps fixes:
 * - Deployment bottlenecks
 * - Manual processes
 * - Environment inconsistencies
 */

// -----------------------------------------------------------------------------
// * HIGH-LEVEL FLOW (MENTAL MODEL)
// -----------------------------------------------------------------------------
/*
 * AGILE FLOW:
 * Plan → Code → Test → Review → Repeat (Sprint)
 *
 * DEVOPS FLOW:
 * Code → Build → Test → Release → Deploy → Monitor → Feedback → Code
 *
 * Agile is cyclical in DEVELOPMENT.
 * DevOps is cyclical in DELIVERY & OPERATIONS.
 */

// -----------------------------------------------------------------------------
// ! KEY DIFFERENCES (SIDE-BY-SIDE)
// -----------------------------------------------------------------------------
/*
 * Agile:
 * - Focus: Development process
 * - Scope: Dev team
 * - Goal: Faster feature delivery
 * - Timeline: Sprint-based
 * - Ends at: "Ready for release"
 *
 * DevOps:
 * - Focus: Delivery & operations
 * - Scope: Dev + Ops + QA + Security
 * - Goal: Fast AND stable releases
 * - Timeline: Continuous
 * - Ends at: Monitoring & feedback
 */

// -----------------------------------------------------------------------------
// ? TOOLS & PRACTICES
// -----------------------------------------------------------------------------
/*
 * Agile uses:
 * - Scrum / Kanban
 * - Backlogs
 * - Sprint planning
 * - Standups
 * - Retrospectives
 *
 * DevOps uses:
 * - CI/CD pipelines
 * - Containers
 * - Cloud infrastructure
 * - Monitoring & logging
 * - Infrastructure as Code
 */

// -----------------------------------------------------------------------------
// ! COMMON MISCONCEPTIONS
// -----------------------------------------------------------------------------
/*
 * ❌ "Agile and DevOps are the same"
 * ❌ "DevOps replaces Agile"
 * ❌ "DevOps is just CI/CD"
 *
 * ✅ Truth:
 * - Agile without DevOps → fast dev, slow release
 * - DevOps without Agile → fast release, wrong features
 */

// -----------------------------------------------------------------------------
// * HOW AGILE & DEVOPS WORK TOGETHER
// -----------------------------------------------------------------------------
/*
 * Agile ensures:
 * - You're building the right thing
 *
 * DevOps ensures:
 * - You can ship it safely and repeatedly
 *
 * Together:
 * - Small changes
 * - Frequent releases
 * - Fast feedback
 * - High quality
 */

// -----------------------------------------------------------------------------
// ! REAL-WORLD EXAMPLE
// -----------------------------------------------------------------------------
/*
 * Agile-only team:
 * - Delivers features every sprint
 * - Deploys once every 2 months
 *
 * Agile + DevOps team:
 * - Delivers features every sprint
 * - Deploys multiple times per day
 * - Monitors production
 * - Rolls back in minutes
 */

// -----------------------------------------------------------------------------
// ? INTERVIEW TRAP QUESTION
// -----------------------------------------------------------------------------
/*
 * ❓ "Can DevOps exist without Agile?"
 *
 * Answer:
 * - Yes, but it's inefficient.
 * - DevOps works best when paired with Agile practices.
 */

// -----------------------------------------------------------------------------
// ! ONE-LINE SENIOR SUMMARY
// -----------------------------------------------------------------------------
/*
 * Agile improves how software is built;
 * DevOps improves how software is shipped and operated.
 *
 * Agile builds fast.
 * DevOps ships fast — and safely.
 */
